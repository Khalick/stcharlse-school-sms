// St. Charles School Management System Service Worker
// Used to demonstrate simulated Background Push Notifications (5 minutes before class alerts)

self.addEventListener('install', (event) => {
  self.skipWaiting();
});

self.addEventListener('activate', (event) => {
  event.waitUntil(self.clients.claim());
});

// Listen for push notifications clicking
self.addEventListener('notificationclick', (event) => {
  event.notification.close();
  
  // Focus window if open, otherwise open app
  event.waitUntil(
    self.clients.matchAll({ type: 'window' }).then((clientList) => {
      for (const client of clientList) {
        if ('focus' in client) {
          return client.focus();
        }
      }
      if (self.clients.openWindow) {
        return self.clients.openWindow('/');
      }
    })
  );
});
